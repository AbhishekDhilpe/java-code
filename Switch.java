public class Switch {
    public static void main(String []args) {
	int i=2;
	switch(i){
		case 1:
			System.out.println("It's One");
			break;
		case 2:
			System.out.println("It's Two");
			break;
		case 3:
			System.out.println("It's Three");
			break;
		default:
			System.out.println("Default");
	}
    }
}








